/** Клиентская фильтрация по подстроке (регистронезависимо), максимум `limit` подсказок. */
export function filterCities(query: string, limit = 6): string[] {
  const q = query.trim().toLowerCase();
  if (!q) return RU_CITIES.slice(0, limit) as string[];
  return RU_CITIES.filter((c) => c.toLowerCase().includes(q)).slice(0, limit);
}
